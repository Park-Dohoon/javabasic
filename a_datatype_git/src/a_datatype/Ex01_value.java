package a_datatype;

public class Ex01_value {

	public static void main(String[] args) {
		
		//변수를 선언
		char testCh;
		int testInt;
		double testFloat;
		boolean testLogic;
		
		//값을 대입
		testCh ='깡';
		testInt = 10000;
		testFloat = 3.6;
		testLogic = true;
		
		//출력
		System.out.println("문자변수: "+testCh);
		System.out.println("정수변수: "+testInt);
		System.out.println("실수변수: "+testFloat);
		System.out.println("논리변수: "+testLogic);
	}

}
